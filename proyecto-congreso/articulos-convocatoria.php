            <h2>Formato</h2>

            Los documentos pueden presentarse en español o en inglés, con una extensión máxima de 25 cuartillas para artículos de investigación; 20 cuartillas para ensayos, y 5 cuartillas para reseñas; incluyendo figuras, tablas y anexos. Todos los trabajos se deberán apegar al sistema APA versión 7 de citas y referencias. <br>

            Todos los documentos deberán incluir nombres completos de autores (sin abreviaturas) y correo electrónico de contacto. <br>
            El documento debe estar escrito con letra Times New Roman a 12 puntos, a espacio y medio, en hoja tamaño carta, formato de procesador de texto Word, con margen en los cuatro costados de 2.5 centímetros. <br>
            Todos los trabajos deberán contener un título (centrado en negritas), resumen y un máximo de cinco palabras clave en español y en inglés. El título describirá el trabajo en un máximo de 150 caracteres y el resumen no será mayor de 250 palabras. <br>
            En caso de incluir figuras, tablas, imágenes o fotografías, estas deberán estar referenciadas en el texto, ser preferentemente a color y ubicarse al final del documento, cada una en hoja separada. <br>
            El texto que describa las figuras estará redactado en forma independiente y no formar parte de la imagen. Las figuras serán enviadas adicionalmente por separado en formato jpg a 300 dpi o superior. <br>
            Las tablas referenciadas en el texto deben de aparecer al final del documento en formato Word y no se incluirán como imágenes. No se repetirá en el texto la información descrita en las tablas y figuras, con excepción de aquella necesaria para la discusión de los resultados. <br>

            <h2>Estructura de Artículos</h2>

            Los artículos se presentarán considerando la estructura siguiente: introducción (máximo 3 cuartillas), metodología, resultados, discusión, conclusiones (estas secciones podrán llevar un título diferente, si así se desea), y referencias. <br>
            Documento en extenso: <br> <br>

            <b>Título:</b> Debe ser breve y no exceder de 15 palabras. <br>
            <b>Resumen:</b> Es una descripción resumida del trabajo; de manera breve, el lector puede saber a priori cómo se realizó la investigación. Deberá contener un máximo de 250 palabras indicando el propósito del trabajo, y tendrá los siguientes elementos: objetivo, material y método, resultados, discusión/conclusión y propuestas. <br>
            <b>Palabras clave:</b> Un mínimo de 3, máximo 5. <br>
            <b>Introducción:</b> Deberá incluir los antecedentes, el planteamiento del problema y el objetivo del estudio en una redacción libre y continua, debidamente sustentada en la bibliografía. <br>
            <b>Material y método:</b> Se señalarán claramente las características de la muestra, los métodos empleados con las referencias pertinentes. En su caso, los métodos estadísticos empleados deberán señalarse claramente con la referencia correspondiente. <br>
            <b>Resultados:</b> Incluirán los hallazgos importantes del estudio, comparándolos –de ser el caso- con las figuras o gráficas estrictamente necesarias y que amplíen la información vertida en el texto. <br>
            <b>Discusión/conclusión:</b> Se contrastarán los resultados con lo informado en la literatura y lo informado en el objetivo y, en su caso, la hipótesis planteada en el trabajo. <br>
            <b>Propuestas:</b> Anotar las recomendaciones que se considere deben desarrollarse derivadas de las conclusiones, para fortalecer la educación a distancia en las IES. <br>
            <b>Referencias:</b> El sistema de referencia para la bibliografía será APA 7ª. Edición. <br>
            <h2>Elementos del Formato</h2>
