    <h1>CAMPEONATO NACIONAL DE VEHÍCULOS IMPULSADOS POR CO2</h1>

    <div class="content">

        <div class="content-main">
            
          El objetivo del concurso es desarrollar tu talento, creatividad y entusiasmo al diseñar y construir un vehículo que permita recorrer en el menor tiempo posible una distancia establecida, con la ayuda de un tanque de CO2 como sistema de propulsión.  
          
            <h2>Requisitos </h2>

            <ul>

              <li>Puede participar cualquier alumno inscrito en una institución pública o privada.</li>
              <li>Al evento puede existir el público en general.</li>
              <li>Cada equipo podrá estar conformado máximo por 2 alumnos(as) por vehículo.</li>
              <li>Tu vehículo debe ser lo suficientemente rígido para soportar un impacto frontal al final de la pista.</li>

            </ul>

            <h2>Especificaciones del Vehículo</h2>

            <h3>Masa</h3>
            No importa el peso, es categoría libre, puede ser tan ligero como tu diseño te lo permita.

            <h3>Diseño</h3>
            La manufactura del vehículo puede ser con cualquier tipo de material, ya sea metal, plástico, madera, etc. No limites la materia prima de construcción de tu vehículo.

            <h3>Área de Trabajo</h3>
            La dimensiones de máximas de trabajo son 240 mm de largo y 90 mm de ancho (puedes utilizar menos área de trabajo que las definidas), como se ilustra en la figura 1 en las que el participante podrá modificar su vehículo a consideración. El diseño deberá de contener una cavidad de 19 mm de diámetro por 51 mm de profundidad situada en la parte trasera del vehículo a 42.5 mm del piso, para poder contener el tanque.

            <h3>Ruedas</h3>
            Las ruedas delanteras como las traseras deberán ser de 39 mm de diámetro por 18 mm de ancho como máximo. Y deberán ser situadas a 9 mm de altura de la base de trabajo.

            <h3>Ejes</h3>
            Podrán ser utilizados ejes de metal de cualquier diámetro el cual debe ser uniforme a lo largo del mismo y en ambos ejes (el diámetro del eje no debe variar), así como rodamientos, arandelas etc. la longitud del eje puede ser hasta 90 mm como máximo para las ruedas delanteras y traseras, no se limita la distancia de centro entre ejes, ni la distancia de centro del eje al frente y final del vehículo.

            <h3>Seguridad de tu vehículo</h3>
            Todo vehículo deberá contener en la parte interior 2 armellas (17x40) ligeramente abiertas para poder asegurar el vehículo a la guía del carril, y que serán situadas aproximadamente 10 puede variar la distancia) del frente y de la parte trasera la cual permitirá al vehículo estar pegado al piso durante todo el trayecto.

            <h3>Especificaciones del tanque</h3>
            El vehículo deberá ser impulsado por una cápsula CO2  de 12gr con dimensiones estandarizadas, las dimensiones son: 18 mm de diámetro y 83 mm de largo.



            <h2>Evaluación</h2>
            <h3>Área de Competencia</h3>
            El área de competencia consta de una pista con dos carriles en línea recta, guiados por un cordel desde el área de disparo, hasta el final donde se encuentra el sensor para la lectura de los tiempos, se prevé de un área lo suficientemente plana para que los vehículos puedan rodar con la menor fricción posible. 	<br>
            El inicio de la competencia estará ubicado en uno de los extremos de la pista el cual contará con el mecanismo de accionamiento del tanque de C02 (véase más especificaciones del mecanismo de accionamiento en este apartado) que será activado por el piloto, cuando el semáforo se lo indique.

            <h3>Meta</h3>
            La meta estará ubicada al sentido opuesto de la línea de salida, la cual contará con un sensor para cada carril para poder capturar el tiempo del recorrido de cada vehículo.

            <h3>Guía de Control</h3>
            Cada carril cuenta con un cordel de 0.5 mm de diámetro el cual se utilizará de guía que cruzará por la mitad de la pista, atravesando todo el largo de cada carril, estará situada a 6 mm del piso.

            <h2>Desarrollo de la Competencia</h2>
            <ul>
              <li>Cada competidor realizará 1 recorrido por la pista, donde se tomarán en forma automática o manual el tiempo de su recorrido, los mejores tiempos podrán clasificar a los 8 mejores.</li>
              <li>En las eliminatorias de los 8 mejores estarán compitiendo 1 a 1 por la clasificación de los 4 mejores tiempos. Esto solo se realizará en un disparo.</li>
              <li>En la semifinal de los 4 mejores estarán compitiendo 1 a 1 por la clasificación de los 2 mejores tiempos para la final y se sacara el tercer lugar. Esto solo se realizará en un disparo.</li>
              <li>La final será 1 a 1 con 2 de 3 disparos para tener los mejores tiempos, el competidor que realice los mejores tiempos será el ganador.</li>
            </ul>

            <h2>Descalificación</h2>
            Un competidor que realice cualquiera de las siguientes acciones será descalificado y obligado a abandonar la competencia. Una persona descalificada no tendrá derecho a recibir reconocimiento electrónico por su participación: <br>

            <ul>
              <li>Cuando un competidor muestra una actitud anti-deportiva. Por ejemplo, expresándose de forma violenta o faltándole el respeto a un oponente o al juez.</li>
              <li>Cuando el vehículo del competidor sufra un accidente y no pueda continuar en la participación. Después de un accidente tendrá 7 minutos máximos para ajustes técnicos y se determinará si continúa.</li>
            </ul>
            
            <h2>Suspensión de la Carrera</h2>
            <ul>
              <li>Un competidor es lastimado de forma significativa.</li>
              <li>Una suspensión de un máximo de 5 minutos puede ser requerido por el competidor para ajustes técnicos en su vehículo en las eliminatorias de 8, 4 y 2 competidores.</li>
            </ul>

            <h2>Reclamos</h2>
            <ul>
              <li>Ningún reclamo al juicio emitido por un juez tendrá validez.</li>
              <li>Un competidor que tenga algún reclamo en cuanto al reglamento, debe expresarlo pacíficamente al comité organizador antes del final de la competencia de manera privada.</li>
            </ul>

            <h2>Concurso</h2>
            El día del evento será el 17 de noviembre del 2022 se realizará el concurso para obtener los 8 mejores tiempos, posteriormente se llevarán los cuartos de final dentro de un horario de 13:00 a 16:00 horas.
            <br> Se solicita puntualidad ya que se dará una capacitación breve de como usar el disparador para evitar penalizaciones.
            <br> Es responsabilidad de los participantes traer sus respectivos tanques de CO2.
            <br> La decisión del jurado será inapelable.
            <br> Lugar del evento: Instalaciones de la Universidad Politécnica de Durango, iniciando el registro a las 12:30 horas
            <br> Los	organizadores	quedan	facultados	a	resolver	los	aspectos	no contemplados en estas bases, así como cualquier eventualidad.

